package com.itheima.arithmeticoperator;

public class ArithmeticoperatorDemo2 {
    public static void main(String[] args) {
//        除法
//        结论
//        1.整数参与运算，只能得到整数
//        2.小数参与运算可能不精确
        System.out.println(10 / 2);
        System.out.println(10 / 3);
        System.out.println(10.0 / 3);

//        取模，取余
        System.out.println(10 % 2);
        System.out.println(10 % 3);

//        应用场景
//        1.判断A是否可以被B整除
//        2.判断A是否为偶数
//        3.斗地主发牌

    }
}
